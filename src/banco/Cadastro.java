    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String nome = textNome.getText();

        String senha1 = textSenha1.getText();
        String senha2 = textSenha2.getText();
        
        textNome.setText("");
        textSenha1.setText("");
        textSenha2.setText("");
        if(nome.equals("")  || senha1.equals("") || senha2.equals("")){
            JOptionPane.showMessageDialog(this, "Preencha todos os campos.");
            return;
        }

        if(!senha1.equals(senha2)){
            JOptionPane.showMessageDialog(this, "As senham n√£o batem.");
            return;
        }

        Conexao conexao = new Conexao(2525);
        try{

            conexao.conectaServidor();
                
            conexao.enviaString("cadastroBanco");
            conexao.enviaString(nome +"&&"+ senha1);

            String result;
            result = conexao.recebeString();

            if(result!=null){

                if(result.equals("true")){
                    JOptionPane.showMessageDialog(this, "Cadastrou");
                    dispose();
                }else{
                   JOptionPane.showMessageDialog(this, "Banco ja existe");
                  
                }

            }

        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Erro no Cadastro");

        }
        finally{
            conexao.fechaConexao();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

