    public void receberAgencias(){
        
        Conexao conexao = new Conexao(2525);
        try{
            
            conexao.conectaServidor();
            
            conexao.enviaString("dadosAgencia");
            conexao.enviaString(nomeBanco);
             
            String result = "";
            result = conexao.recebeString();
            
            agList = new DefaultListModel();
           
            if(result!=null){
                String[] quebra = result.split("&&");
               
                for(int i=0; i<quebra.length; i++){
                    
                    agList.add(i, quebra[i]);
                }
                 
            }
            jListAgencias.setModel(agList);
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Erro na busca de agencias");
        }finally{
            if(conexao.isConected()) conexao.fechaConexao();
        }
        
        
    }
   
