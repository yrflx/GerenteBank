    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String numero = JOptionPane.showInputDialog("Digite o numero da agencia");
        
        if(numero.equals("")){
            JOptionPane.showMessageDialog(this,"Informe o numero da agencia");
        }else{
             Conexao conexao = new Conexao(2525);
            try{
            
                conexao.conectaServidor();

                conexao.enviaString("cadastroAgencia");
                conexao.enviaString(nomeBanco+"&&"+numero);

                String result = "";
                result = conexao.recebeString();

                if(result.equals("true")){
                    JOptionPane.showMessageDialog(this, "Cadastro realizado;");
                    receberAgencias();
                    
                    
                }else{
                    JOptionPane.showMessageDialog(this, "Erro no cadastro da Agencia");
                }
            
            }catch(Exception e){
                 JOptionPane.showMessageDialog(this, "Erro no cadastro da Agencia\nerr:" + e);
            }finally{
                if(conexao.isConected()) conexao.fechaConexao();
            }
            
             
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
         
        if(jListAgencias.getSelectedIndex()== -1){
            JOptionPane.showMessageDialog(this, "Selecione uma agencia");
        }else{
            int dialogResult = JOptionPane.showConfirmDialog (null, "Você deseja remover a agencia " + jListAgencias.getSelectedValue() ,"ATENÇÃO",JOptionPane.YES_NO_OPTION);

            if(dialogResult == JOptionPane.YES_OPTION){

                Conexao conexao = new Conexao(2525);
                try{
            
                    conexao.conectaServidor();

                    conexao.enviaString("removeAgencia");
                    conexao.enviaString(nomeBanco+"&&"+jListAgencias.getSelectedValue());

                    String result = "";
                    result = conexao.recebeString();

                    if(result.equals("true")){
                        JOptionPane.showMessageDialog(this, "Agencia removida;");
                        receberAgencias();


                    }else{
                        JOptionPane.showMessageDialog(this, "Erro ao remover Agencia");
                    }
            
                }catch(Exception e){
                     JOptionPane.showMessageDialog(this, "Erro ao remover Agencia \n err:" + e);
                }finally{
                    if(conexao.isConected()) conexao.fechaConexao();
                }
                
               

            }

        }
        
    }//GEN-LAST:event_jButton5ActionPerformed

